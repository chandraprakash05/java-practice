package main.java.practice.loops;

public class ForHW {
    public static void main(String[] args) {
        // Print a diamond
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 5; j++) {

                if (j==(5 + 1)/2) {
                    System.out.print("*");

                }
            }
        }
    }
}

