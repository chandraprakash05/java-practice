package main.java.practice;

public class ArraysPractice {
    public static void main(String[] args) {
        int[] a = new int[4];
        a[0] = 1;
        a[1] = 5;
        a[2] = 17;
        a[3] = 25;
        for (int i = 0; i < a.length; i++) {
            System.out.println(a[i]);
        }
    }
}
